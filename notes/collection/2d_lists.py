# 2dlist = [list1, list2, list3] -> two dimensional 
# it's like a matrix

fruits =     ['apple', 'pineapple', 'banana']
vegetables = ['pumpkin', 'potatoes', 'carrots', 'lettuce']
drinks =     ['milk', 'wine', 'milkshake']

grocery = [fruits, vegetables, drinks]

print(grocery[2])           # to access any row
print(grocery[1][2])        # two indices to access an element
print(grocery)